const mongoose = require("mongoose");
const Scheme = mongoose.Schema;

const UserSchema = new Scheme({
  user: {
    type: String
  },
  email: {
    type: String,
    required: true
  },
  password: {
    type: String,
    required: true
  },
  date: {
    type: Date,
    default: Date.now
  }
});

module.exports = User = mongoose.model("user", UserSchema);
